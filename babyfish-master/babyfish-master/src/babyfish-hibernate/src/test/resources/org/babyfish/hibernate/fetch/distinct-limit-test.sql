CREATE TABLESPACE BABYFISH_DISTINCT_LIMIT_DATA
    LOGGING
    DATAFILE ' BABYFISH_DISTINCT_LIMIT_DATA.DBF' 
    SIZE 32M 
    AUTOEXTEND ON 
    NEXT 32M MAXSIZE 2048M
    EXTENT MANAGEMENT LOCAL;

